import { NextRequest } from 'next/server';
import { ReadWorkflow, EditWorkflow, RemoveWorkflow } from '@/app/api/modules/workflows/controllers';

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return ReadWorkflow(request, id);
}

export async function PATCH(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return EditWorkflow(request, id);
}

export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return RemoveWorkflow(request, id);
}
