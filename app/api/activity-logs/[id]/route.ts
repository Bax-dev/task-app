import { NextRequest } from 'next/server';
import { handleGetActivityLog, handleUpdateActivityLog, handleDeleteActivityLog } from '@/app/api/modules/activity-logs/controllers';

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleGetActivityLog(request, id);
}

export async function PATCH(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleUpdateActivityLog(request, id);
}

export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleDeleteActivityLog(request, id);
}
